char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int g;

	i = 0;
	g = 0;
	while (dest[i] != '\0')
		i++;
	while (src[g] != '\0')
	{
		dest[i] = src[g];
		i++;
		g++;
		if (src[g] == '\0')
			dest[i] = '\0';
	}
	return (dest);
}
