#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int g;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		else
		{
			g = n + '0';
			write(fd, &g, 1);
		}
	}
}
